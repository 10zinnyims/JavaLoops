public class WhileLoop {
    public static void main(String[] args) {
        int count = 1;

        while (count <= 5) {
            System.out.println("I love Java");
            count++;
        }
        int count2 = 1;
        do {
            System.out.println("I love Java");
            count2++;

        } while (count2 <= 5);
    }
}

